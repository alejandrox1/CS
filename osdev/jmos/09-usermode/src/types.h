#ifndef _TYPES_H
#define _TYPES_H

/*
 * typedefs to standardize sizes across platforms.
 * Writfen for 32-bit x86.
 */
typedef unsigned int   uint32_t;
typedef          int   int32_t;
typedef unsigned short uint16_t;
typedef          short int16_t;
typedef unsigned char  uint8_t;
typedef          char  int8_t;


#endif // _TYPES_H
